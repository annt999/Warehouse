<?php

namespace App\Helpers;

use App\Models\Order;
use App\Models\Product;
use App\Models\WareHouse;
use Carbon\Carbon;

class Common
{
    public static function vn_to_str ($str){

        $unicode = array(

            'a'=>'á|à|ả|ã|ạ|ă|ắ|ặ|ằ|ẳ|ẵ|â|ấ|ầ|ẩ|ẫ|ậ',

            'd'=>'đ',

            'e'=>'é|è|ẻ|ẽ|ẹ|ê|ế|ề|ể|ễ|ệ',

            'i'=>'í|ì|ỉ|ĩ|ị',

            'o'=>'ó|ò|ỏ|õ|ọ|ô|ố|ồ|ổ|ỗ|ộ|ơ|ớ|ờ|ở|ỡ|ợ',

            'u'=>'ú|ù|ủ|ũ|ụ|ư|ứ|ừ|ử|ữ|ự',

            'y'=>'ý|ỳ|ỷ|ỹ|ỵ',

            'A'=>'Á|À|Ả|Ã|Ạ|Ă|Ắ|Ặ|Ằ|Ẳ|Ẵ|Â|Ấ|Ầ|Ẩ|Ẫ|Ậ',

            'D'=>'Đ',

            'E'=>'É|È|Ẻ|Ẽ|Ẹ|Ê|Ế|Ề|Ể|Ễ|Ệ',

            'I'=>'Í|Ì|Ỉ|Ĩ|Ị',

            'O'=>'Ó|Ò|Ỏ|Õ|Ọ|Ô|Ố|Ồ|Ổ|Ỗ|Ộ|Ơ|Ớ|Ờ|Ở|Ỡ|Ợ',

            'U'=>'Ú|Ù|Ủ|Ũ|Ụ|Ư|Ứ|Ừ|Ử|Ữ|Ự',

            'Y'=>'Ý|Ỳ|Ỷ|Ỹ|Ỵ',

        );

        foreach($unicode as $nonUnicode=>$uni){

            $str = preg_replace("/($uni)/i", $nonUnicode, $str);

        }

        return $str;

    }
    public static function generateProductCode()
    {
        $warehouseId = sprintf('%02d', auth()->user()->warehouse_id);
        $latestProduct = Product::orderBy('created_at','DESC')->first();
        $productCode = '#'.str_pad(is_null($latestProduct) ? 0 : $latestProduct->id, 8, "0", STR_PAD_LEFT);
        return $warehouseId . $productCode;
    }

    public static function generateOrderCode(): string
    {
        $warehouseId = sprintf('%02d', auth()->user()->warehouse_id);
        $latestOrder = Order::orderBy('created_at','DESC')->first();
        $orderCode = '#'.str_pad(is_null($latestOrder) ? 0 : $latestOrder->id, 8, "0", STR_PAD_LEFT);
        return $warehouseId . $orderCode;
    }
}
